print("10%2 =",10%2);
print("10%4 =",10%4);

print("// Operator gives the Quotient corresponding Remainder as Integer only division");
print("10//2 =",10%2);
print("10//4 =",10%4);

print("Note: Remainder is always a positive number!!!");
print("-10//3 =",(-10//3));
